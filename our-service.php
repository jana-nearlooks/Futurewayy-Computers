                            <ul>
                                <li><strong>Normal/full  backups</strong> All files  that have been  selected are backed up, regardless of the setting of the archive   attribute. When a file is backed up, the archive attribute is cleared.  If the  file is later modified, this attribute is set, which indicates  that the file  needs to be backed up.</li>
                                <li><strong>Copy  backups</strong> All files  that have been selected  are backed up, regardless of the setting of the archive  attribute.  Unlike a normal backup, the archive attribute on files isn't  modified.  This allows you to perform other types of backups on the files at a   later date.</li>
                                <li><strong>Differential  backups</strong> Designed to  create backup  copies of files that have changed since the last normal backup.  The  presence of the archive attribute indicates that the file has been  modified  and only files with this attribute are backed up. However, the  archive  attribute on files isn't modified. This allows you to perform  other types of  backups on the files at a later date.</li>
